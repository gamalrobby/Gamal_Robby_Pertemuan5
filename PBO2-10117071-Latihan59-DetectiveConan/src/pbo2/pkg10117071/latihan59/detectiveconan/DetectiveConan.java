package pbo2.pkg10117071.latihan59.detectiveconan;

/**
 *
 * @author Destroy Eyes
 */
public class DetectiveConan {
    private final String nama;
    private final String sifat;
    private final String peran;

    public DetectiveConan(String nama, String sifat, String peran) {
        this.nama = nama;
        this.sifat = sifat;
        this.peran = peran;
    }

    public String getNama() {
        return nama;
    }

    public String getSifat() {
        return sifat;
    }

    public String getPeran() {
        return peran;
    }

    
}
